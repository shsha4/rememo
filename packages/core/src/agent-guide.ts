export const AGENT_GUIDE = `# rememo vault — LLM 에이전트 맥락 가이드

이 파일은 vault 루트의 \`AGENTS.md\`다. Claude Code · Codex 같은 **LLM 에이전트가 이 vault를
"지식 그래프"로 읽고 쓰기** 위한 규약이며, 에이전트가 이 디렉터리에서 작업할 때 자동으로 참조된다.

---

## 1. 원칙 — 파일이 곧 그래프

- **진실의 원천은 \`.md\` 파일이다.** vault 안의 모든 지식과 관계는 마크다운 텍스트에 인코딩돼 있다.
- \`.memograph/index.db\`(SQLite)는 **데스크톱 GUI 전용 파생 캐시**다. 에이전트는 **이 DB를 읽지 않는다.**
  앱을 켜지 않은 상태에서 파일을 만들면 이 캐시는 갱신되지 않으므로 신뢰하면 안 된다. 파일만 본다.
- 관계는 파일에서 **읽는 즉시 최신**이다. 별도 색인 단계가 필요 없다 — 이것이 SQLite 대신 파일을 원천으로 삼는 이유다.

## 2. 문법 — 관계를 이렇게 표현한다

| 요소 | 문법 | 의미 |
|---|---|---|
| 노트 제목 | 파일명(확장자 제외) | \`박민준.md\` → 제목 \`박민준\` |
| 위키링크 | \`[[대상]]\` | 다른 노트로의 **명시적 관계**(엣지) |
| 별칭 링크 | \`[[대상\\|보이는 텍스트]]\` | 관계는 \`대상\`, 화면엔 별칭 |
| 헤딩 링크 | \`[[대상#헤딩]]\` | 노트의 특정 섹션 참조 |
| 태그 | \`#태그\`, \`#상위/하위\` | 분류. 한글·중첩 지원 |
| frontmatter | 파일 맨 위 \`---\` YAML 블록 | 메타데이터(\`key: value\`, \`key: [a, b]\`) |

- **링크 해석 규칙**: \`[[대상]]\`은 먼저 제목이 \`대상\`인 노트로 매칭한다. 없으면 현재 노트 기준 상대경로 \`대상.md\`로 본다.
- **엔티티 멘션(자동 감지 관계)**: 본문에 다른 노트의 제목이 그대로 등장하면(한국어 조사 허용: \`박민준과\`, \`카프카에서\`) GUI는 이를 약한 관계로 잡는다. 이는 파일에 명시돼 있지 않고 계산으로 나온다 —
  **에이전트는 명시적 \`[[링크]]\`만 확실한 관계로 신뢰하고, 나머지 언급은 의미로 판단한다.**

## 3. 맥락 수집법

- **아웃링크(이 노트가 가리키는 것)**: 노트 본문의 \`[[...]]\`를 모은다.
- **백링크(이 노트를 가리키는 것)**: vault 전체에서 \`[[대상제목]]\`을 grep한다.
- **이웃(1-hop)**: 아웃링크 ∪ 백링크.
- **N-hop 확장**: 이웃의 이웃으로 넓혀가며 관련 노트 뭉치를 만든다.
- **태그로 모으기**: 같은 \`#태그\`를 가진 노트들을 grep으로 묶는다.

수동 grep/read가 부담되면 **\`rememo\` CLI**를 쓴다(§5). CLI는 SQLite가 아니라 파일을 즉석 파싱하므로 항상 최신이다.

## 4. 쓰기 규약

- 새 노트는 **기존 노트를 최소 1개 \`[[링크]]\`로 연결**해 그래프에서 고립되지 않게 한다.
- 제목은 파일명이 된다 — 파일명을 신중히 정한다(제목이 곧 링크 대상).
- frontmatter 키는 vault 안에서 **일관되게** 쓴다(예: \`type\`, \`tags\`, \`created\`).
- 마크다운 표준을 따른다. 자동 저장은 없으니(데스크톱 앱 기준) 파일 저장은 확실히 한다.

## 5. CLI (\`rememo\`) — 선택적 조회 도구

\`packages/cli\`가 제공한다. **SQLite를 쓰지 않고 호출 시점에 \`.md\`를 파싱**하므로 데몬·앱 없이 항상 최신 결과를 준다.

\`\`\`bash
# 한 노트의 맥락(본문 + 아웃링크 + 백링크 + 태그 + 이웃)을 한 번에
rememo context "박민준" --vault /path/to/vault --json

# vault 전체 그래프(노드 + 엣지)
rememo graph --vault /path/to/vault --json

# 제목/본문 검색
rememo search "카프카" --vault /path/to/vault --json
\`\`\`

- \`--vault DIR\` 생략 시 현재 디렉터리를 vault로 본다.
- \`--json\`은 기계 판독용(에이전트 권장). 생략하면 사람이 읽기 좋은 텍스트.
- \`--hops N\`(\`context\`): 이웃 확장 깊이(기본 1, 0이면 이웃 없음).
`;

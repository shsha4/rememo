/**
 * 노트 경로를 DB/인덱스의 식별자로 쓸 수 있도록 정규화한다.
 *
 * Windows에서는 코드 경로마다 구분자가 달라진다(파일 다이얼로그·path.join은 역슬래시 `\`,
 * 렌더러에서 문자열로 조합하면 슬래시 `/`). 같은 파일이 서로 다른 문자열로 저장되면
 * 노트/할 일이 중복되고 그래프 엣지 JOIN이 어긋난다.
 *
 * 그래서 모든 경로를 **슬래시(`/`)로 통일**해 같은 파일이 항상 하나의 문자열이 되게 한다.
 * (Node의 fs는 Windows에서도 슬래시 경로를 정상 처리하므로 파일 입출력에도 안전하다.)
 */
export function normalizeNotePath(p: string): string {
  return p.replace(/\\/g, '/');
}
